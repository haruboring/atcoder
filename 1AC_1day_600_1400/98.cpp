#define debug(x) cerr << #x << ": " << x << endl
using namespace std;
using ll = long long;

const int MAX = 1020000;
ll MOD = 1000000000 + 7;
ll fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++) {
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD % i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
ll COM(int n, int k) {
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

int main() {
    int N, K;
    cin >> N >> K;

    // blue: K, red: N - K
    ll MOD = 1e9 + 7;

    COMinit();
    for (int i = 1; i <= K; i++) {
        ll ans = COM(N - K + 1, i) * COM(K - 1, i - 1) % MOD;
        cout << ans << endl;
    }
}